/*
FOLDERS AND FUNCTIONS
.idea : - This holds configurations for android studio
android : - This contains our compile flutter project into android app
build : - This holds the output of your flutter application
ios : - Just like the android folder, this contains our flutter compiled ios app
lib : - This stands for library and it is in this folder we will do 99% of our work, here we will have all our dart files for writing our codes.
test : - This folder contains automated tests for our application
pubspec.yaml : - This is a file inside where the dependencies of the project is managed (See this as our package.json) file
pubspec.lock : - This is a file that holds more details of the dependencies in pubspec.yaml (See this as our package-lock.json)
*/
